    private void jbAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbAlterarActionPerformed
        SalvarAlterar = "alterar";
        int linha = this.jtFuncionario.getSelectedRow();
        try {
            int codigoFuncionario = (int) this.jtFuncionario.getValueAt(linha, 0);
            //Recupera dados do banco//
            modelFuncionarios = controllerFuncionarios.retornarFuncionarioController(codigoFuncionario);
            //Setar na interface//
            this.jtfCodigo.setText(String.valueOf(modelFuncionarios.getIdFuncionario()));
            this.jtfNome.setText(modelFuncionarios.getFunNome());
            this.jtfEmail.setText(modelFuncionarios.getFunEmail());
            this.jtfTelefone.setText(modelFuncionarios.getFunTelefone());
            this.jtfCPF.setText(modelFuncionarios.getFunCPF());
            this.jtfSenha.setText(modelFuncionarios.getFunCPF());
            this.jtfCargo.setText(modelFuncionarios.getFunCargo());
            this.jtfLogradouro.setText(modelFuncionarios.getFunLogradouro());
            this.jtfComplemento.setText(modelFuncionarios.getFunComplemento());
            this.jtfBairro.setText(modelFuncionarios.getFunBairro());
            this.jtfCidade.setText(modelFuncionarios.getFunCidade());
           //Obter a UF selecionada na ComboBox
            String ufSelecionada = (String) jcbUF.getSelectedItem();
            modelFuncionarios.setFunUF(ufSelecionada);
            this.jtfCEP.setText(modelFuncionarios.getFunCEP());
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Código inválido ou nenhum registro selecionado", "AVISO",JOptionPane.ERROR_MESSAGE);
        }finally{
            carregarFuncionario();
            habilitarDesabilitarCampos(true);
    }                                         

    }//GEN-LAST:event_jbAlterarActionPerformed

