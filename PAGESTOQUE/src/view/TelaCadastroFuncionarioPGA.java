    private void salvarFuncionario(){
        if(SalvarAlterar.equals("salvar")){
        modelFuncionarios.setFunNome(this.jtfNome.getText());
        modelFuncionarios.setFunEmail(this.jtfEmail.getText());
        modelFuncionarios.setFunTelefone(this.jtfTelefone.getText());
        modelFuncionarios.setFunCPF(this.jtfCPF.getText());
        modelFuncionarios.setFunSenha(this.jtfSenha.getText());
        modelFuncionarios.setFunCargo(this.jtfCargo.getText());
        modelFuncionarios.setFunLogradouro(this.jtfLogradouro.getText());
        modelFuncionarios.setFunComplemento(this.jtfComplemento.getText());
        modelFuncionarios.setFunBairro(this.jtfBairro.getText());
        modelFuncionarios.setFunCidade(this.jtfCidade.getText());
        //Obter a UF selecionada na ComboBox
            String ufSelecionada = (String) jcbUF.getSelectedItem();
            modelFuncionarios.setFunUF(ufSelecionada);
        modelFuncionarios.setFunCEP(this.jtfCEP.getText());
        
       if (controllerFuncionarios.salvarFuncionarioController(modelFuncionarios) > 0){
           JOptionPane.showMessageDialog(this, "Funcionário cadastrado com sucesso!", "ATENÇÃO", JOptionPane.WARNING_MESSAGE);
           carregarFuncionario();
           limparCampos();
           habilitarDesabilitarCampos(false);
       }else{
           JOptionPane.showMessageDialog(this, "Erro ao cadastrar o funcionário!", "ERRO", JOptionPane.ERROR_MESSAGE);
       }
    
        }else if (SalvarAlterar.equals("alterar")){
            this.AlterarFuncionario();
            carregarFuncionario();
    }    
    }
    private void AlterarFuncionario(){
       modelFuncionarios.setFunNome(this.jtfNome.getText());
        modelFuncionarios.setFunEmail(this.jtfEmail.getText());
        modelFuncionarios.setFunTelefone(this.jtfTelefone.getText());
        modelFuncionarios.setFunCPF(this.jtfCPF.getText());
        modelFuncionarios.setFunSenha(this.jtfSenha.getText());
        modelFuncionarios.setFunCargo(this.jtfCargo.getText());
        modelFuncionarios.setFunLogradouro(this.jtfLogradouro.getText());
        modelFuncionarios.setFunComplemento(this.jtfComplemento.getText());
        modelFuncionarios.setFunBairro(this.jtfBairro.getText());
        modelFuncionarios.setFunCidade(this.jtfCidade.getText());
        //modelFuncionarios.setFunUF(this.jtfUF.getText());
        modelFuncionarios.setFunCEP(this.jtfCEP.getText());
        
       if (controllerFuncionarios.alterarFuncionarioController(modelFuncionarios)){
           JOptionPane.showMessageDialog(this, "Funcionário alterado com sucesso!", "ATENÇÃO", JOptionPane.WARNING_MESSAGE);
           carregarFuncionario();
           limparCampos();
       }else{
           JOptionPane.showMessageDialog(this, "Erro ao alterar o Funcionário!", "ERRO", JOptionPane.ERROR_MESSAGE);
       }
    }
