    /**
     * Preenche a tabela de produtos com os produtos cadastrados no banco
     */
    private void carregarFuncionario(){
       listaModelFuncionario = controllerFuncionarios.retornarListaFuncionariosController();
       DefaultTableModel modelo = (DefaultTableModel) jtFuncionario.getModel();
       modelo.setNumRows(0);
       
       //inserir produtos na tabela
       int cont = listaModelFuncionario.size();
       for (int i = 0; i < cont; i++) {
           modelo.addRow(new Object[]{
           listaModelFuncionario.get(i).getIdFuncionario(),
           listaModelFuncionario.get(i).getFunNome(),
           listaModelFuncionario.get(i).getFunEmail(),
           listaModelFuncionario.get(i).getFunTelefone(),
           listaModelFuncionario.get(i).getFunCPF(),
           listaModelFuncionario.get(i).getFunSenha(),
           listaModelFuncionario.get(i).getFunCargo(),
           listaModelFuncionario.get(i).getFunLogradouro(), 
           listaModelFuncionario.get(i).getFunComplemento(),
           listaModelFuncionario.get(i).getFunBairro(),
           listaModelFuncionario.get(i).getFunCidade(),
           listaModelFuncionario.get(i).getFunUF(),
           listaModelFuncionario.get(i).getFunCEP(),
       });
       }
    }
    
    

