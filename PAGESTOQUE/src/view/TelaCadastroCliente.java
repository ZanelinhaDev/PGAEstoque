    /*Salva produto no banco*/
    private void salvarCliente(){
        if(SalvarAlterar.equals("salvar")){
        modelClientes.setCliNome(this.jtfNome.getText());
        modelClientes.setCliEmail(this.jtfEmail.getText());
        modelClientes.setCliTelefone(this.jtfTelefone.getText());
        modelClientes.setCliCPF(this.jtfCPF.getText());
        modelClientes.setCliLogradouro(this.jtfLogradouro.getText());
        modelClientes.setCliComplemento(this.jtfComplemento.getText());
        modelClientes.setCliBairro(this.jtfBairro.getText());
        modelClientes.setCliCidade(this.jtfCidade.getText());
     //Obter a UF selecionada na ComboBox
            String ufSelecionada = (String) jcbUF.getSelectedItem();
            modelClientes.setCliUF(ufSelecionada);
        modelClientes.setCliCEP(this.jtfCEP.getText());
      if (controllerClientes.salvarClienteController(modelClientes)> 0){
           JOptionPane.showMessageDialog(this, "Cliente cadastrado com sucesso!", "ATENÇÃO", JOptionPane.WARNING_MESSAGE);
           carregarClientes();
           limparCampos();
           habilitarDesabilitarCampos(false);
       }else{
           JOptionPane.showMessageDialog(this, "Erro ao cadastrar o cliente!", "ERRO", JOptionPane.ERROR_MESSAGE);
       }
    
        }else if (SalvarAlterar.equals("alterar")){
            this.alterarCliente();
            carregarClientes();
    }    
    }   
           
