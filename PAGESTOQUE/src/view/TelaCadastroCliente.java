    private void jbExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbExcluirActionPerformed
      int linha = jtClientes.getSelectedRow();
        int codigoCliente = (int) jtClientes.getValueAt(linha, 0);
        
        if (controllerClientes.excluirClienteController(codigoCliente)) {
            JOptionPane.showMessageDialog(this, "Cliente excluido com sucesso!", "ATENÇÃO", JOptionPane.WARNING_MESSAGE);
            carregarClientes();
        }else{
            JOptionPane.showMessageDialog(this, "Erro ao exlcuir o cliente", "ERRO", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jbExcluirActionPerformed

    private void jbAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbAlterarActionPerformed
        SalvarAlterar = "alterar";
        int linha = this.jtClientes.getSelectedRow();
        try {
            int codigoCliente = (int) this.jtClientes.getValueAt(linha, 0);
            //Recupera dados do banco//
            modelClientes = controllerClientes.retornarClienteController(codigoCliente);
            //Setar na interface//
            this.jtfCodigo.setText(String.valueOf(modelClientes.getIdCliente()));
            this.jtfNome.setText(modelClientes.getCliNome());
            this.jtfEmail.setText(modelClientes.getCliEmail());
            this.jtfTelefone.setText(modelClientes.getCliTelefone());
            this.jtfCPF.setText(modelClientes.getCliCPF());
            this.jtfLogradouro.setText(modelClientes.getCliLogradouro());
            this.jtfComplemento.setText(modelClientes.getCliComplemento());
            this.jtfBairro.setText(modelClientes.getCliBairro());
            this.jtfCidade.setText(modelClientes.getCliCidade());
            //Obter a UF selecionada na ComboBox
            String ufSelecionada = (String) jcbUF.getSelectedItem();
            modelClientes.setCliUF(ufSelecionada);
            this.jtfCEP.setText(modelClientes.getCliCEP());
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Código inválido ou nenhum registro selecionado", "AVISO",JOptionPane.ERROR_MESSAGE);
        }finally{
            carregarClientes();
            habilitarDesabilitarCampos(true);
    }                                         

    }//GEN-LAST:event_jbAlterarActionPerformed

