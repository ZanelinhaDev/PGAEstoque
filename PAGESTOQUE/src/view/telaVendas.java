    private void jbSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbSalvarActionPerformed
        int codigoVenda = 0, codigoProduto = 0;
        double desconto = 0;
        listaModelProdutos = new ArrayList<>();
        modelProdutos = new ModelProdutos();

// VALIDAÇÃO PARA DESCONTO
        if (jtfDesconto.getText().equals("")) {
            desconto = 0;
        } else {
            desconto = Double.parseDouble(jtfDesconto.getText());
        }

        modelVendas.setVenda_cliente(Integer.parseInt(jtfCodigoCli.getText()));

// CONVERTENDO DATA
        try {
            modelVendas.setVenDataVenda(datas.converterDataParaDateUS(new java.util.Date(System.currentTimeMillis())));
        } catch (Exception ex) {
            Logger.getLogger(telaVendas.class.getName()).log(Level.SEVERE, null, ex);
        }

        modelVendas.setVenValorLiquido(Double.parseDouble(jtfValorTotal.getText()));
        modelVendas.setVenValorBruto(Double.parseDouble(jtfValorTotal.getText()) + desconto);
        modelVendas.setVenDesconto(desconto);

// Salvando venda    
        codigoVenda = controllerVendas.salvarVendasController(modelVendas);
        if (codigoVenda > 0) {
            JOptionPane.showMessageDialog(this, "Saída salva com sucesso!", "Atenção", JOptionPane.WARNING_MESSAGE);
            carregarVendas();
            limparCampos();
            
            // Lista de Produtos agora é criada aqui
            

            int cont = jtProdutosVendas.getRowCount();
            for (int i = 0; i < cont; i++) {
                codigoProduto = ((int) jtProdutosVendas.getValueAt(i, 0));
                modelVendasProdutos = new ModelVendasProdutos();
                modelVendasProdutos.setProduto(codigoProduto);
                modelVendasProdutos.setVendas(codigoVenda);
                modelVendasProdutos.setVen_pro_valor((double) jtProdutosVendas.getValueAt(i, 3));
                modelVendasProdutos.setVen_pro_quantidade((int) jtProdutosVendas.getValueAt(i, 2));

                listaModelVendasProdutos.add(modelVendasProdutos);

                // produto 
                modelProdutos.setIdProduto(codigoProduto);
                modelProdutos.setProQuantidade(controllerProdutos.retornarProdutoController(codigoProduto).getProQuantidade()
                        - Integer.valueOf(jtProdutosVendas.getValueAt(i, 2).toString()));
                listaModelProdutos.add(modelProdutos);
            }

            // Salvando Produtos após o loop
            if (controllerVendasProdutos.salvarVendasProdutoController(listaModelVendasProdutos)) {
                controllerProdutos.alterarQuantidadeProdutoController(listaModelProdutos);

                JOptionPane.showMessageDialog(this, "Produto da saída salvo com sucesso", "Atenção", JOptionPane.WARNING_MESSAGE);
                carregarVendas();
                limparCampos();
            } else {
                JOptionPane.showMessageDialog(this, "Erro ao salvar", "Erro", JOptionPane.ERROR_MESSAGE);
            }
        } else {
            JOptionPane.showMessageDialog(this, "Erro ao salvar saída!", "Erro", JOptionPane.ERROR_MESSAGE);
        }

    }//GEN-LAST:event_jbSalvarActionPerformed

